#include <stdio.h>
#include <stdlib.h>
int vowel (char c)
{
    c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u';
    return c;
}
int main(void)
{
    char(v);
    scanf ("%c",&v);
    if (vowel(v))
        printf("this is vowel %c ", v);
    else
        printf("%c", v);
    return 0;
}
